fn main() {
    println!("January has");

    println!("{} days and {} hours", 31i, 4i);

    println!("{0}, this is {1}. {1} this is {0}", "Alice", "Bob");

    println!("{subject} {verb} {predicate}", predicate="over the lazy dog", subject="The quick brown fox", verb="jumps");

    println!("{} of {:t} people know binary, the other half don't", 1i, 2i);

    println!("My name is {0}, {1} {0}", "Bond", "James");
}
